package com.na982.opichelper.domain.entity

enum class ModeGroup { NONE, REPEAT_LISTENING, ENGLISH_WRITING, FULL_MEMORIZATION }

enum class CurrentMode {
    NONE,
    QUESTION_PLAY,
    ANSWER_PLAY,
    REPEAT_LISTENING,
    ENGLISH_WRITING,
    ENGLISH_WRITING_RECORDING,
    ENGLISH_WRITING_PLAYING,
    ENGLISH_WRITING_WITH_FILE,
    FULL_MEMORIZATION,
    FULL_MEMORIZATION_QUESTION_PLAYING,
    FULL_MEMORIZATION_RECORDING,
    FULL_MEMORIZATION_PLAYING,
    FULL_MEMORIZATION_WITH_FILE;

    val group: ModeGroup get() = when (this) {
        NONE -> ModeGroup.NONE
        QUESTION_PLAY -> ModeGroup.NONE
        ANSWER_PLAY -> ModeGroup.NONE
        REPEAT_LISTENING -> ModeGroup.REPEAT_LISTENING
        ENGLISH_WRITING, ENGLISH_WRITING_RECORDING, ENGLISH_WRITING_PLAYING, ENGLISH_WRITING_WITH_FILE -> ModeGroup.ENGLISH_WRITING
        FULL_MEMORIZATION, FULL_MEMORIZATION_QUESTION_PLAYING, FULL_MEMORIZATION_RECORDING, FULL_MEMORIZATION_PLAYING, FULL_MEMORIZATION_WITH_FILE -> ModeGroup.FULL_MEMORIZATION
    }
}
